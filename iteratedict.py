#Write a Python program to iterate over dictionaries using for loops.
mydict0 = {
            'key0000000' : 0,
            'key000000' : 00,
            'key00000' : 000,
            'key0000' : 0000,
            'key000' : 00000,
            'key00' : 000000,
            'key0' : 0000000,
}

for key, value in mydict0.items():
    print(key, value)
